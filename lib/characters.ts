// Регулярний вираз для пошуку імені перед двокрапкою на початку абзацу.
// Будуємо з масиву CHARACTERS — найдовші імена першими (вже відсортовано вище),
// щоб "Дід Панас" не давав часткове співпадіння з "Панас".
// Екрануємо спецсимволи регексу (« » дужки тощо), бо в іменах вони є.
function escapeRegex(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

